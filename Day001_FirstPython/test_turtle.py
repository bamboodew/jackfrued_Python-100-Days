import turtle

turtle.pensize(4) # 线条粗细
turtle.pencolor('red') # 线条颜色
turtle.forward(200) # 向前200px
turtle.right(144) # 右拐144°
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.mainloop()
